export const getRandomGamertag = () => {
  // eslint-disable-next-line no-constant-condition
  while (true) {
    const gamerTag = getRandomGamerTagAnyLenght();
    if (gamerTag.length <= 16) {
      return gamerTag;
    }
  }
};

const getRandomGamerTagAnyLenght = () => {
  const randomIndex1 = Math.floor(Math.random() * FIRST_WORDS.length);
  const randomIndex2 = Math.floor(Math.random() * LAST_WORDS.length);
  const firstWords = FIRST_WORDS[randomIndex1];
  const lastWords = LAST_WORDS[randomIndex2];
  const firstArray = Array.isArray(firstWords) ? firstWords : [firstWords];
  const lastArray = Array.isArray(lastWords) ? lastWords : [lastWords];
  const combinedArray = [...firstArray, ...lastArray];
  const lowerCaseWords = combinedArray.map((word) => word.toLowerCase());

  return formatWithRandomCase(lowerCaseWords);
};

function capitalize(word: string) {
  return word.charAt(0).toUpperCase() + word.slice(1);
}

function toPascalCase(words: string[]) {
  return words.map(capitalize).join('');
}

function toCamelCase(words: string[]) {
  return words
    .map((word, index) => {
      if (index === 0) {
        return word;
      }
      return capitalize(word);
    })
    .join('');
}

function toSnakeCase(words: string[]) {
  return words.join('_').toLowerCase();
}

function toCapsSnakeCase(words: string[]) {
  return words.join('_').toUpperCase();
}

function toKebabCase(words: string[]) {
  return words.join('-').toLowerCase();
}

const CASE_FUNCTIONS = [
  toPascalCase,
  toCamelCase,
  toSnakeCase,
  toCapsSnakeCase,
  toKebabCase,
];

function formatWithRandomCase(words: string[]) {
  const randomIndex = Math.floor(Math.random() * CASE_FUNCTIONS.length);
  return CASE_FUNCTIONS[randomIndex](words);
}

